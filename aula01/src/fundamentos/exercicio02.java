package fundamentos;

public class exercicio02 {

	public static void main(String[] args) {
		int a = 10;
		int b =-10;
		boolean c = true;
		boolean d = false;
		System.out.println(~a); // -11 (Inverte o sinal e subtrai com 1)
		System.out.println(~b); // 9 (Inverte o sinal e subtrai com 1)
		System.out.println(!c); // false (o oposto do valor booleano)
		System.out.println(!d); // true (o oposto do valor booleano)
		
	}

}
